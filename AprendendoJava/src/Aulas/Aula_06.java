package Aulas;

public class Aula_06{
    public static void main(String[]args){
       //Os quatros abaixos são capazes de armazenar apenas números inteiros 
       byte a; //Suporta apenas números pequenos
       short b; // Suporta números mais ou menos piquenis
       int c; // suporta números grandes
       long d; // suporta números enormes
       
       //Os dois abaixos são capazes de armazenar números decimais
       float e; //Suporta números decimais com pouca presisão
       double f;//Suporta número decimais com grande pesisão
       
       //Boolean serve apenas para armazenar falores de True e False
       //char serve para armazenar um caracterie, como letro por exemplo
       boolean g; //Suporta apenas valores de true e false 
       char h; //Suporta apenas caractere com aspas simples
    }
}
